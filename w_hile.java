public class w_hile {

  public static void main(String[] args) {
      int numWhile = 0;

      while (numWhile < 3){
          System.out.println("While: "+numWhile);
          numWhile++;
      }
  }

}
/*En el bucle while, la condición booleana se evalúa antes de que se ejecute el código.*/



